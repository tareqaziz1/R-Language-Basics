#Control Flow in R

# Condition -

If-else
x <- 10
if(x > 5){
  print("Big")
} else {
  print("Small")
}

# for and while loop


#for loop
for(i in 1:5){
  print(i)
}

#while loop
while loop
i <- 1
while(i <= 5){
  print(i)
  i <- i + 1
}

# repeat loop

repeat {
  # Code to execute
  if (condition) {
    break
  }
}
